import java.util.*;
import java.lang.*;

public class BrandSold {
    private String name;
    private int total;

    public BrandSold() {
    }

    public BrandSold(String name, int total) {
        this.name = name;
        this.total = total;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    @Override
    public String toString() {
        return "BrandSold{" + "name=" + name + ", total=" + total + '}';
    }
    
    
}
