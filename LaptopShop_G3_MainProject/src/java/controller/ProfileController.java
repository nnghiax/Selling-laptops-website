    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("user");
        if (user.getRole_id().equals("4")) {
            request.setAttribute("user", user);
            request.getRequestDispatcher("./profile.jsp").forward(request, response);
        } else {
            response.sendRedirect("login");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        HttpSession session = request.getSession();
        User userLogin = (User) session.getAttribute("user");
        if (userLogin == null || (userLogin != null && !userLogin.getRole_id().equals("4"))) {
            response.sendRedirect("login");
            return;
        }
        UserDBContext userDAO = new UserDBContext();
        int userId = Integer.parseInt(request.getParameter("user_id"));
        String userName = request.getParameter("user_name");
        String userEmail = request.getParameter("user_email");
        String userPhone = request.getParameter("user_phone");
        String userImage = request.getParameter("user_image");
        boolean emailExists = userDAO.isEmailExists(userEmail, userId);
        boolean phoneExists = userDAO.isPhoneExists(userPhone, userId);

        if (emailExists || phoneExists) {
            request.setAttribute("error", "Email or Phone number already exists.");
            doGet(request, response);
            return;
        }
        userLogin.setUser_email(userEmail);
        userLogin.setUser_phone(userPhone);
        userLogin.setUser_image(userImage);
        userDAO.updateUserProfile(userLogin);
        request.setAttribute("success", "Update profile successfully.");
        session.setAttribute("user", userLogin);
        doGet(request, response);
        return;
    }

